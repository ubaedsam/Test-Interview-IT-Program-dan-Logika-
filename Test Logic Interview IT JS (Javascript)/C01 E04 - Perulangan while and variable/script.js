// Pengulangan While and Variable

console.log("LOOPING PERTAMA");
var loop = 2;
while (loop < 22) {
    console.log(loop + " - I love coding");
    loop += 2;
}
console.log("LOOPING KEDUA");
loop -= 2;
while (loop >= 2) {
    console.log(loop + " - I will become fullstack developer");
    loop -= 2;
}